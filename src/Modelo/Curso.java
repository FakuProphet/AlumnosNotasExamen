package Modelo;

/**
 *
 * @author alumno
 */
public class Curso {
    
    private int codigo;
    private String nombre;
    private String turno;

    public Curso(int codigo, String nombre, String turno) {
        this.codigo = codigo;
        this.nombre = nombre;
        this.turno = turno;
    }

    

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTurno() {
        return turno;
    }

    public void setTurno(String turno) {
        this.turno = turno;
    }

    @Override
    public String toString() {
        return nombre;
    }

  
    
    
}
