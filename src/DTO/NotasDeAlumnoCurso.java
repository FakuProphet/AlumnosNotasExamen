package DTO;

/**
 *
 * @author alumno
 */
public class NotasDeAlumnoCurso {
    
    private int nota;
    private String alumno;
    private String curso;

    public NotasDeAlumnoCurso(int nota, String alumno, String curso) {
        this.nota = nota;
        this.alumno = alumno;
        this.curso = curso;
    }

    public int getNota() {
        return nota;
    }

    public void setNota(int nota) {
        this.nota = nota;
    }

    public String getAlumno() {
        return alumno;
    }

    public void setAlumno(String alumno) {
        this.alumno = alumno;
    }

    public String getCurso() {
        return curso;
    }

    public void setCurso(String curso) {
        this.curso = curso;
    }
    
    
    
}
